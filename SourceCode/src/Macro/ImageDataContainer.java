package Macro;

public class ImageDataContainer
{
	public String image;
	public int imageX,imageY,imageWidth,imageHeight;
	ImageDataContainer(String image,int imageX,int imageY,int imageWidth,int imageHeight)
	{
		this.image=image;
		this.imageX=imageX;
		this.imageY=imageY;
		this.imageWidth=imageWidth;
		this.imageHeight=imageHeight;
	}
}
